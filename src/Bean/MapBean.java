package Bean;

public class MapBean {
	private String tourSiteTitle;
	private double tourSiteMapX;
	private double tourSiteMapY;
	public String getTourSiteTitle() {
		return tourSiteTitle;
	}
	public void setTourSiteTitle(String tourSiteTitle) {
		this.tourSiteTitle = tourSiteTitle;
	}
	public double getTourSiteMapX() {
		return tourSiteMapX;
	}
	public void setTourSiteMapX(double tourSiteMapX) {
		this.tourSiteMapX = tourSiteMapX;
	}
	public double getTourSiteMapY() {
		return tourSiteMapY;
	}
	public void setTourSiteMapY(double tourSiteMapY) {
		this.tourSiteMapY = tourSiteMapY;
	}
}
